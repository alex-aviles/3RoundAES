# printState function to print 4x4 bytes
def printState(state):
    for row in range(4):
        print(" ".join(f"{state[row][col]:02x}" for col in range(4)))

# textToState function to convert ascii text to a 4x4 state
def textToState(text: ascii) -> list:
    text = bytes(text, 'utf-8')
    if len(text) < 16:
        text += b' ' * (16 - len(text))
    state = [[0] * 4 for ir in range(4)]
    for i in range(16):
        row = i % 4 
        col = i // 4
        state[row][col] = text[i]
    return state

# SubWord function subs provided bytes using Substitution Box SBox
def SubWord(word: bytes) -> bytes:
    subWordBytes = []
    for byte in word:
        row = byte >> 4
        col = byte & 0x0f 
        subbedByte = SBox[row][col]
        subWordBytes.append(subbedByte)
    return bytes(subWordBytes)

# RotWord function rotates bytes left by 1
def RotWord(word: bytes) -> bytes:
    rotWordBytes = word[1:] + word[:1]
    return rotWordBytes

# Rcon function returns the round constant associated with the provided int
def Rcon(round: int) -> list:
    rconBytes = [RconList[round], 0x00, 0x00, 0x00]
    return rconBytes

# KeyExpansion function takes 16 byte key and expands the key length to the number of rounds * 16
def KeyExpansion(key: bytes, numOfRounds: int) -> list:
    words = len(key) // 4 
    numOfBytes = 4  
    rounds = numOfRounds
    total = numOfBytes * (rounds + 1)
    roundKeyWords = [list(key[i:i+4]) for i in range(0, len(key), 4)]
    
    i = 1
    while len(roundKeyWords) < total:
        newKey = list(roundKeyWords[-1]) 
        if len(roundKeyWords) % words == 0:
            newKey.append(newKey.pop(0))
            newKey = [SBoxList[b] for b in newKey]
            newKey[0] ^= RconList[i]
            i += 1
        elif words == 8 and len(roundKeyWords) % words == 4:
            newKey = [SBoxList[b] for b in newKey]
        
        newKey = [newKey[j] ^ roundKeyWords[-words][j] for j in range(4)]
        roundKeyWords.append(newKey)
    
    roundKeys = [roundKeyWords[4 * i: 4 * (i + 1)] for i in range(len(roundKeyWords) // 4)]
    return roundKeys

# SubBytes function subs all bytes in the state
def SubBytes(state: list) -> list:
    for row in range(4):
        for col in range(4):
            byte = state[row][col]
            state[row][col] = SBox[byte >> 4][byte & 0x0F]
    return state

# ShiftRows function shifts each state row variably
def ShiftRows(state: list) -> list:
    for col in range(1, 4):  
        for ir in range(col):  
            partial = state[0][col]
            for row in range(3):
                state[row][col] = state[row + 1][col]
            state[3][col] = partial
    return state

# MixColumns function performs column-wise algebraic math on bytes
def MixColumns(state: list) -> list:
    def multiply(byte, multiplier):
        result = 0
        for ir in range(8):
            if multiplier & 1:
                result ^= byte
            carry = byte & 0x80
            byte = (byte << 1) & 0xFF
            if carry:
                byte ^= 0x1B
            multiplier >>= 1
        return result

    for i in range(4):
        column = state[i]
        byte1 = column[0]
        byte2 = column[1]
        byte3 = column[2]
        byte4 = column[3]

        state[i][0] = (multiply(byte1, 2) ^ multiply(byte2, 3) ^ byte3 ^ byte4)
        state[i][1] = (byte1 ^ multiply(byte2, 2) ^ multiply(byte3, 3) ^ byte4)
        state[i][2] = (byte1 ^ byte2 ^ multiply(byte3, 2) ^ multiply(byte4, 3))
        state[i][3] = (multiply(byte1, 3) ^ byte2 ^ byte3 ^ multiply(byte4, 2))
    return state

# AddRoundKey function xor's specific round key to state
def AddRoundKey(state: list, roundKey: list) -> list:
    for row in range(4):
        for col in range(4):
            state[row][col] ^= roundKey[row][col]
    return state

# AESRound performs each round of AES transformations including the last without MixColumns
def AESRound(state: list, roundKey: list, finalRound=False) -> list:
    print("Before SubBytes:")
    printState(state)
    state = SubBytes(state)
    print("After SubBytes:")
    printState(state)
    state = ShiftRows(state)
    print("After ShiftRows:")
    printState(state)
    if not finalRound:
        state = MixColumns(state)
        print("After MixColumns:")
        printState(state)
    state = AddRoundKey(state, roundKey)
    print("After AddRoundKey:")
    printState(state)
    return state

